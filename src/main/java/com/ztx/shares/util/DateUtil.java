    public static String getOldDay(String pattern,int days,Date date){
        SimpleDateFormat format = new SimpleDateFormat(pattern);
        Calendar c = Calendar.getInstance();
        c.setTime(new Date());
        c.add(Calendar.DATE, -days);
        Date d = c.getTime();
        String day = format.format(d);
        return day;
    }

    public static String getOldMouth(String pattern,int mouth,Date date){
        SimpleDateFormat format = new SimpleDateFormat(pattern);
        Calendar c = Calendar.getInstance();
        c.setTime(new Date());
        c.add(Calendar.MONTH, -mouth);
        Date m = c.getTime();
        String mon = format.format(m);
        return mon;
    }

    public static String getOldyear(String pattern,int year,Date date){
        SimpleDateFormat format = new SimpleDateFormat(pattern);
        Calendar c = Calendar.getInstance();
        c.setTime(date);
        c.add(Calendar.MONTH, -year);
        Date m = c.getTime();
        String mon = format.format(m);
        return mon;
    }
